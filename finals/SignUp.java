    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String fullname = jTextFieldfulname.getText();
        String email = jTextFieldemail.getText();
        String password = jTextFieldPassword.getText();
        String confirm = jTextFieldconfirmPassword.getText();

        // Empty field validation
        if (fullname.isEmpty() || email.isEmpty() || password.isEmpty() || confirm.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill out all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Email validation
        if (!email.contains("@") || !email.contains(".")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email.", "Invalid Email", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Password match validation
        if (!password.equals(confirm)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Success message
        JOptionPane.showMessageDialog(this, "Account created successfully!");

        // OPTIONAL: Go to Login page
        Login loginPage = new Login();  
        loginPage.setVisible(true);
        this.dispose();
    
    }//GEN-LAST:event_jButton1ActionPerformed

